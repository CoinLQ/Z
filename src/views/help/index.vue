				<p>为了避免用户误操作，程序控制用户不可以删除正文以及参考标点，仅支持用户输入汉字标点"<em class="fb">！。、？，；：</em>"等七种标点，以及对换行符的增加和删除操作。</p>
				<p>未完成任务时，用户可以点击暂存，以便下次可以继续。完成任务后，可以点击完成，将任务提交给系统。</p>
